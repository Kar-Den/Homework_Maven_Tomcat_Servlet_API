package com.academy;

public class SimpleCalc {
    public double sum (double a, double b){
        return a + b;
    }
    public double minus (double a, double b){
        return a - b;
    }
    public double mul (double a, double b){
        return a * b;
    }
    public double div (double a, double b){
        return a / b;
    }

}
